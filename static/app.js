  function convertToTraditional(text) {
    return String(text || "")
      .split("")
      .map((char) => SIMPLIFIED_TO_TRADITIONAL_MAP[char] || char)
      .join("");
  }

  function translateToEnglish(text) {
    const source = String(text || "");
    if (!source.trim()) return source;
    if (ENGLISH_PHRASES[source]) return ENGLISH_PHRASES[source];
    let translated = source;
    const entries = Object.entries(ENGLISH_PHRASES).sort((left, right) => right[0].length - left[0].length);
    for (const [from, to] of entries) {
      if (!from || !translated.includes(from)) continue;
      translated = translated.split(from).join(to);
    }
    return translated;
  }

  function translateUiText(text, language) {
    if (language === "zh-Hant") return convertToTraditional(text);
    if (language === "en") return translateToEnglish(text);
    return String(text || "");
  }

  function shouldSkipTranslationNode(node) {
    const parent = node?.parentElement;
    if (!parent) return true;
    return Boolean(parent.closest("script, style, noscript, code, pre, canvas, svg"));
  }

  function applyLanguageToDom(language) {
    const root = document.getElementById("app");
    if (!root) return;
    document.documentElement.lang = language;

    const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
      acceptNode(node) {
        if (shouldSkipTranslationNode(node)) return NodeFilter.FILTER_REJECT;
        return node.nodeValue && node.nodeValue.trim()
          ? NodeFilter.FILTER_ACCEPT
          : NodeFilter.FILTER_REJECT;
      },
    });

    const textNodes = [];
    while (walker.nextNode()) {
      textNodes.push(walker.currentNode);
    }
    for (const node of textNodes) {
      const cached = translationTextCache.get(node);
      const current = node.nodeValue;
      let source = cached?.source ?? current;
      if (!cached || (current !== cached.source && current !== cached.translated)) {
        source = current;
      }
      const translated = translateUiText(source, language);
      if (current !== translated) {
        node.nodeValue = translated;
      }
      translationTextCache.set(node, { source, translated });
    }

    const elements = root.querySelectorAll(TRANSLATABLE_ATTRIBUTES.map((attr) => `[${attr}]`).join(","));
    for (const element of elements) {
      let cachedAttributes = translationAttributeCache.get(element);
      if (!cachedAttributes) {
        cachedAttributes = {};
        translationAttributeCache.set(element, cachedAttributes);
      }
      for (const attr of TRANSLATABLE_ATTRIBUTES) {
        if (!element.hasAttribute(attr)) continue;
        const current = element.getAttribute(attr) || "";
        if (!current.trim()) continue;
        const cached = cachedAttributes[attr];
        let source = cached?.source ?? current;
        if (!cached || (current !== cached.source && current !== cached.translated)) {
          source = current;
        }
        const translated = translateUiText(source, language);
        if (current !== translated) {
          element.setAttribute(attr, translated);
        }
        cachedAttributes[attr] = { source, translated };
      }
    }
  }

